
public class AssignmentOperator {

	public static void main(String[] args) {

		/*
		 # 대입 연산자 (=, +=, -=, *=, /=, %=)
		 - 변수에 값을 대입할 때 사용하는 연산자 입니다.
		 */
		
		int a = 5, b = 5;
		
		a += 3; // a = a + 3
		
		System.out.println(a);
		
		System.out.println("--------------------------");
		
		
		a -= 4; //4
		System.out.println(a);
		
		a *= 6; //24
		System.out.println(a);
		
		a /= 5; //4
		System.out.println(a);
		
		a %= 3; //1
		System.out.println(a);
	}

}
